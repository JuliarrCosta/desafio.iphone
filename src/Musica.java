
public class Musica {

    private String nome;
    private String cantor;
 
    public Musica(String nome, String cantor) {
        this.nome = nome;
        this.cantor = cantor;
      
    }

    public String getNome() {
        return this.nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCantor() {
        return this.cantor;
    }

    public void setCantor(String cantor) {
        this.cantor = cantor;
    }

   
}
